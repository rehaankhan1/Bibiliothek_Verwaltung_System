    private void LogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInActionPerformed
        String passwordVal = password.getText();
        String usernameVal = username.getText();
        
        if(usernameVal.isEmpty() || usernameVal.isBlank()|| passwordVal.isEmpty() || passwordVal.isBlank() || usernameVal.isEmpty()&&passwordVal.isEmpty() || usernameVal.isBlank()&&passwordVal.isBlank()) 
            JOptionPane.showMessageDialog(null, "Invalid Entry!");
        else{
       
       // UserAuthentication.SignIn(usernameVal, passwordVal, Dbconn.Connect());
      if( UserAuthentication.SignIn(usernameVal, passwordVal, Dbconn.Connect())==true){
          JOptionPane.showMessageDialog(null, "You are now logged in!");
          Session sb=new Session(true, usernameVal);
          Dashboard db=new Dashboard(sb);
          db.setVisible(true);
         this.setVisible(false);
      }else{
          JOptionPane.showMessageDialog(null, "Wrong username & password combination!");
      }
    }
    }//GEN-LAST:event_LogInActionPerformed

