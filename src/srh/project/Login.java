    private void createUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserActionPerformed
        String passwordVal = password.getText();
        String usernameVal = username.getText();
        
        if(usernameVal.isEmpty() || usernameVal.isBlank()|| passwordVal.isEmpty() || passwordVal.isBlank() || usernameVal.isEmpty()&&passwordVal.isEmpty() || usernameVal.isBlank()&&passwordVal.isBlank()) 
            JOptionPane.showMessageDialog(null, "Invalid Entry!");
        else{
             /* generates the Salt value. It can be stored in a database. */  
        String saltvalue = PassBasedEnc.getSaltvalue(30);  
        
         /* generates an encrypted password. It can be stored in a database.*/  
        String encryptedpassword = PassBasedEnc.generateSecurePassword(passwordVal, saltvalue);  
        
        
        if(Dbconn.CheckUser(usernameVal)==0) {
            UserAuthentication.createUser(usernameVal, encryptedpassword, saltvalue, Dbconn.Connect());
        }else{
           JOptionPane.showMessageDialog(null, "Username already exists");
        }
        
        }
         
    }//GEN-LAST:event_createUserActionPerformed

